    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {

        RequestDispatcher dispatcher = null;
        int otpvalue = 0;
        HttpSession mySession = request.getSession();
        UserDAO uDao = new UserDAO();

        //get email send from client
        String email = request.getParameter("email");

        //check emty value
        if (!email.isEmpty()) {
            //check if email exist or not
            boolean isMailExist = uDao.isMailExist(email);
            if (isMailExist) {
                //Create random otp to send to user
                Random rand = new Random();
                otpvalue = rand.nextInt(1255650);

                //send email to user with otp
                EmailService.sendEmailOtp(email, String.valueOf(otpvalue));
                
                
                //assign value of email and otp to session
                mySession.setAttribute("otp", otpvalue);
                mySession.setAttribute("email", email);
                
                
                //after send email redirect to enter otp page
                dispatcher = request.getRequestDispatcher("/views/EnterOtp.jsp");
                mySession.setAttribute("message", "OTP is sent to your email");
                dispatcher.forward(request, response);
                
            } else {
                mySession.setAttribute("msg", "Email not exist! You can try again!");
                response.sendRedirect("home");
            }
        }
    }

